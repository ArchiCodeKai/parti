/** 依 slug 取流派 */
export function getMovementBySlug(slug: string): Movement | undefined {
  return MOVEMENTS.find((m) => m.id === slug);
}

/** 取某流派的核心建築師（從 ARCHITECTS 即時推導，依重要度排序） */
export function getArchitectsByMovement(movementId: string): Architect[] {
  return ARCHITECTS.filter((a) =>
    a.movements.some((m) => m.id === movementId),
  ).sort((a, b) => b.importance - a.importance);
}

/** 取流派配色（找不到回退灰色） */
export function getMovementColor(movementId: string): string {
  return getMovementBySlug(movementId)?.colorTheme ?? "#7A7A7A";
}
